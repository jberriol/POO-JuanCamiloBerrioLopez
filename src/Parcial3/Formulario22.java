    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nombre;
        double salariohora,horastrabajadas, salariomensual;
        
        
        horastrabajadas = Double.parseDouble(txtHorasTrabajadas.getText());
        salariohora = Double.parseDouble(txtSalarioHora.getText());
        nombre = txtNombre.getText();
        
        salariomensual = horastrabajadas*salariohora;
        
        if (salariomensual>450000){
            txtSalida.setText("El nombre del empleado es "+ nombre+" y su salario es "
                    + salariomensual);}
        else txtSalida.setText("El nombre del empleado es "+ nombre);
        
    }//GEN-LAST:event_jButton1ActionPerformed

