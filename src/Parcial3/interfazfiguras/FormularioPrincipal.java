    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Cuadrado figura1 = new Cuadrado(Integer.parseInt(txtLadoCuadrado.getText()));
        Rectangulo figura2 = new Rectangulo(Integer.parseInt(txtBaseRectangulo.getText()),Integer.parseInt(txtAlturaRectangulo.getText()));
        Circulo figura3 = new Circulo(Integer.parseInt(txtRadioCirculo.getText()));
        TrianguloRectangulo figura4 = new TrianguloRectangulo(Integer.parseInt(txtBaseTrianguloRe.getText()),Integer.parseInt(txtAlturaTrianguloRe.getText()));
        
        txtSalida.setText("El area del cuadrado es "+ figura1.calcularArea()+" y su perimetro es "+figura1.calcularPerimetro()+"\n"
                + "El area del rectangulo es "+figura2.calcularArea()+" y su perimetro es "+figura2.calcularPerimetro()+"\n"
                        + "El area del circulo es "+ figura3.calcularArea()+" y su perimetro es "+figura3.calcularPerimetro()+"\n"
                                + "El area del triangulo rectangulo es "+figura4.calcularArea()+", su perimetro es "+ figura4.calcularPerimetro()+" y finalmente, "+figura4.determinarTipoTriangulo());
        
    }//GEN-LAST:event_jButton1ActionPerformed

