    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        double A, B, C, discriminante, sol1, sol2,sol,partereal,parteimag;
        
        A = Double.parseDouble(txtA.getText());
        
        B = Double.parseDouble(txtB.getText());
        
        C = Double.parseDouble(txtC.getText());
        
        discriminante = Math.pow(B,2) - 4*A*C;

        if (discriminante > 0){
            sol1 = (-B + Math.sqrt(discriminante))/(2*A);
            sol2 = (-B - Math.sqrt(discriminante))/(2*A);
            txtSalida.setText("Las soluciones son " + sol1 + " y " + sol2);}
         else if (discriminante == 0){
            sol = -B / (2 * A);
            txtSalida.setText("La soluci√≥n es" + sol);}
         else {
            partereal = -B/(2*A);
            parteimag = Math.sqrt(-discriminante)/(2*A);
            txtSalida.setText("Las soluciones son " + partereal + " + " 
                    + parteimag + "i y " + 
                    partereal + " - " + parteimag + "i");}
    }//GEN-LAST:event_jButton1ActionPerformed

